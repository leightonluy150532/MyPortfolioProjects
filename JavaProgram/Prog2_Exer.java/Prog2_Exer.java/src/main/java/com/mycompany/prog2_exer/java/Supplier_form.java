    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
                   DefaultTableModel suppTable = (DefaultTableModel) stable.getModel();
         if (supp_num == 0)
            suppTable.setRowCount(0);
            int addid = addId();
            supplier[supp_num][0] = String.valueOf(addid);
            supplier[supp_num][1] = sname.getText();
            supplier[supp_num][2] = cp.getText();
            supplier[supp_num][3] = num.getText();
            supplier[supp_num][4] = email.getText();
            messagebox("Save?", "Record");
            suppTable.addRow(supplier[supp_num]);
            supp_num++;
            
            
            
            
            
            showRecords();
    }//GEN-LAST:event_savebtnActionPerformed

