    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
          DefaultTableModel suppTable = (DefaultTableModel) stable.getModel();
          for (int i=0; i< supp_num; i++)  {    
            if (supplier[i][0].equals(SUPID.getText())){
                    suppTable.setRowCount(0);
                    supplier[i][1] = sname.getText();
                    supplier[i][2] = cp.getText();
                    supplier[i][3] = num.getText();
                    supplier[i][4] = email.getText();      
            }
        } 
        messagebox("Edit?", "Record");
        showarray();  
    }//GEN-LAST:event_editbtnActionPerformed

    private void delebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delebtnActionPerformed
        // TODO add your handling code here:
                        for (int x = 0; x < supp_num; x++){
           
            if (supplier[x][0].equals(SUPID.getText())){
                  int y=x;
                while (y<supp_num){
                    supplier[y][0]=supplier[y+1][0];
                    supplier[y][1]=supplier[y+1][1];
                    supplier[y][2]=supplier[y+1][2];
                    supplier[y][3]=supplier[y+1][3];
                    supplier[y][4]=supplier[y+1][4];
                    y++;
                }
                break;
            } 
        }
        supp_num --;
        messagebox("Delete?","Record");
        showarray();
    }//GEN-LAST:event_delebtnActionPerformed
   public void showRecords(){                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                        
        for (int i = 0; i < supplier.length; i++) {
            for (int j = 0; j < supplier[0].length; j++) {
                System.out.print(Arrays.toString(supplier[j]));
            }
        }
    }
      public void showarray(){
        DefaultTableModel suppTable = (DefaultTableModel) stable.getModel();
            suppTable.setRowCount(0);
            for (int i = 0; i< supp_num;i++){
                if (supplier[i][0]!= null);
                suppTable.addRow(supplier[i]);
            }
    }
  
   public int addId(){
        String [][] suppl_id = new String[supp_num][1];
        for(int i = 0; i < supp_num; i++){
            suppl_id[i][0] = supplier[i][0];
        }
        for (int i = 0; i < suppl_id.length - 1; i++){
          for(int j = 0; j < suppl_id.length - 1 - i;j++){
              int a = Integer.parseInt(suppl_id[j][0]);
              int b = Integer.parseInt(suppl_id[j+1][0]);
              
              if(a > b){
                  String temp = suppl_id[j][0];
                  suppl_id[j][0] = suppl_id[j+1][0];
                  suppl_id[j+1][0]= temp;
              }
          }
        }
        
         int newidno = 1;
        for (int g = 0; g < suppl_id.length ; g++)
          {
                int current_id = Integer.parseInt(suppl_id[g][0]);
                if(current_id == newidno){
                      newidno++; 
                } else{
                    break;
                }
                
          }
          return newidno;
   }
