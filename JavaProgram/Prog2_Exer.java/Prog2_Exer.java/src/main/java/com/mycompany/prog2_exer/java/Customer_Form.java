    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
                DefaultTableModel cust_table = (DefaultTableModel) ctable2.getModel();
               
        if (cust_num == 0) 
            cust_table.setRowCount(0);
        int newid = id();
        customers[cust_num][0] = String.valueOf(newid);
        customers[cust_num][1] = name.getText();
        customers[cust_num][2] = address.getText();
        customers[cust_num][3] = contact.getText();
        customers[cust_num][4] = email.getText();
        customers[cust_num][5] = birthday.getText();
        customers[cust_num][6] = String.valueOf(gender.getSelectedItem());
         messagebox("Save?","Record");
        
        cust_table.addRow(customers[cust_num]);
               
         
         cust_num ++;
        
        
        
        showRecords();
        
        
    }//GEN-LAST:event_savebtnActionPerformed

