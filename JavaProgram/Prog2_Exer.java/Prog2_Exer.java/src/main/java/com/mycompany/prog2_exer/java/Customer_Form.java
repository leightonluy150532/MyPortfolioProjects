public int id(){
    String [][] curr_id = new String[cust_num][1];
    for (int i = 0; i< cust_num; i++){
         curr_id[i][0] = customers[i][0];
    }
    for (int i=0; i<curr_id.length -1 ; i++){
        for(int j=0; j<curr_id.length -1 - i;j++){
            int a = Integer.parseInt(curr_id[j][0]);
            int b = Integer.parseInt(curr_id[j+1][0]);
            if (a > b){
                String temp = curr_id[j][0];
                curr_id[j][0] = curr_id[j+1][0];
                curr_id[j+1][0]= temp;
            }
        }
    }
        int newidno = 1;
    for(int g = 0; g < curr_id.length; g++)
    {
            int  latestidno = Integer.parseInt(curr_id[g][0]);
                if(latestidno == newidno){
                    newidno++;
                }
                else{
                    break;
                }
    }
    
    return newidno;
}
    

