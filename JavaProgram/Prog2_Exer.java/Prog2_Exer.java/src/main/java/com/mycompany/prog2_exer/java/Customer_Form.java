    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here: 
        for (int i=0; i < cust_num; i++){
            int y = i;
            if(customers[i][0].equals(id.getText())){
               while (y < cust_num){
                   customers[y][0] = customers[y+1][0];
                   customers[y][1] = customers[y+1][1];
                   customers[y][2] = customers[y+1][2];
                   customers[y][3] = customers[y+1][3];
                   customers[y][4] = customers[y+1][4];
                   customers[y][5] = customers[y+1][5];
                  y++;
               }
               break;
             }
                 
        }
        messagebox("Delete?","Record");
        cust_num --;
        showarray();
    }//GEN-LAST:event_deletebtnActionPerformed

