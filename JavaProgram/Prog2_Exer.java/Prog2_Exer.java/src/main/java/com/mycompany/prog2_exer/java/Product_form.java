        public int sort(){
          String [][] prodId = new String [prod_no][1];
          for(int i = 0; i< prod_no; i++){
              prodId[i][0] = products[i][0];
          }
          for(int i = 0; i<prodId.length - 1; i++){
              for(int j = 0; j < prodId.length - 1 - i;j++){
                  int a = Integer.parseInt(prodId[j][0]);
                  int b = Integer.parseInt(prodId[j+1][0]);
                  
                  if (a > b){
                      String temp = prodId[j][0];
                      prodId[j][0] = prodId[j+1][0];
                      prodId[j+1][0] = temp;
                  }  
              }
          }
          int newprodno = 1;
          for (int k =0; k < prodId.length;k++){
              int newprodId = Integer.parseInt(prodId[k][0]);
              if(newprodId == newprodno){
                  newprodno++;
              } else{
                  break;
              }
          }
          return newprodno;
      }
    public void showRecords(){                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                        
        for (int i = 0; i < products.length; i++) {
            for (int j = 0; j < products[0].length; j++) {
                System.out.print(Arrays.toString(products[j]));
            }
        }
    }
        public void showarray(){
        DefaultTableModel prod_table = (DefaultTableModel) prodTable.getModel();
            prod_table.setRowCount(0);
            for (int i = 0; i< prod_no;i++){
                if (products [i][0]!= null);
                prod_table.addRow(products[i]);
            }
    }   
