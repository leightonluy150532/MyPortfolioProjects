    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
         DefaultTableModel prod_table = (DefaultTableModel) prodTable.getModel();     
        for(int i = 0; i < prod_no;i++){
            if(products[i][0].equals(pID.getText())){
               prod_table.setRowCount(0);
               products[i][1] = pType.getText();
               products[i][2] = PrDesc.getText();
               products[i][3] = Spply.getText();
        }
        }
        messagebox("Edit?", "Record");
        showarray();
        
    }//GEN-LAST:event_editbtnActionPerformed

    private void delebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delebtnActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
           for (int x = 0; x < prod_no; x++){
                int y=x;
                if (products[x][0].equals(pID.getText())){
                while (y < prod_no){
                    products[y][0]=products[y+1][0];
                    products[y][1]=products[y+1][1];
                    products[y][2]=products[y+1][2];
                    products[y][3]=products[y+1][3];
                    y++;
                }
                break;
            } 
            
        }
        messagebox("Delete?", "Record");
        prod_no--;
        showarray();   
                     
    }//GEN-LAST:event_delebtnActionPerformed

    private void stockinbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stockinbtnActionPerformed
        // TODO add your handling code here:
              DefaultTableModel prod_table = (DefaultTableModel) prodTable.getModel();
       if (prod_no == 0)
            prod_table.setRowCount(0);
            int piddy = sort();
            products[prod_no][0] = String.valueOf(piddy);
            products[prod_no][1] = pType.getText();
            products[prod_no][2] = PrDesc.getText(); 
            products[prod_no][3] = Spply.getText();
            messagebox("Save?", "Record");
            prod_table.addRow(products[prod_no]);
            
            prod_no++;
           
            
            showRecords();
    }//GEN-LAST:event_stockinbtnActionPerformed

